# Faca um programa que solicite ao usuarios as 4 notas de atividades
# continuas e mostre a media. Lembrando que, o total de atividades 
# continuas consideradas para o calculo da media sera um total de sete.
nota1 = int(input("Digite a primeira nota: "))
nota2 = int(input("Digite a segunda nota: "))
nota3 = int(input("Digite a terceira nota: "))
nota4 = int(input("Digite a quarta nota: "))

media = (nota1+nota2+nota3+nota4)/4

print("A media e: ", media)
