#  Faça um programa que calcule a área de um quadrado, em seguida 
#  mostre o dobro desta área para o usuário
altura = float(input("Entre com o valor da altura: "))
largura = float(input("Entre com o valor da largura: "))

area = altura * largura

print("O dobro da area eh: ", float(area * 2))

